EditField::EditField(int x, int y, int w) :
  Widget(x, y, w, 1), _focus(false)
{ }


void EditField::move(int dy)
{
  _y += dy;
  delwin(_win);
  _win = subwin(stdscr, _h, _w, _y, _x);
}

